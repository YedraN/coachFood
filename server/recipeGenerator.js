function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function normalizeName(name) {
  return name
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9\s]/g, '')
    .trim();
}

function scoreRecipe(recipe, pantryNames) {
  const recipeIngredientNames = recipe.ingredients.map(i => normalizeName(i.name));
  const pantryNormalized = pantryNames.map(normalizeName);

  const matches = recipeIngredientNames.filter(name =>
    pantryNormalized.some(pantry => pantry.includes(name) || name.includes(pantry))
  );

  return {
    uses: matches.length,
    total: recipeIngredientNames.length,
    score: recipeIngredientNames.length > 0 ? matches.length / recipeIngredientNames.length : 0,
  };
}

function pickRecipesForTag(recipes, pantryNames, count, existingIds) {
  const scored = recipes
    .filter(r => !existingIds.has(r.title))
    .map(r => ({ recipe: r, match: scoreRecipe(r, pantryNames) }))
    .sort((a, b) => b.match.score - a.match.score);

  const top = scored.filter(s => s.match.score > 0);
  const fallback = scored.filter(s => s.match.score === 0);

  const candidates = top.length >= count ? shuffle(top) : [...shuffle(top), ...shuffle(fallback)];

  return candidates.slice(0, count);
}

function distributeMealTypes(selected) {
  const tags = ['Desayuno', 'Comida', 'Cena'];
  const counts = { Desayuno: 0, Comida: 0, Cena: 0 };
  selected.forEach(r => { counts[r.tag] = (counts[r.tag] || 0) + 1; });

  const missing = tags.filter(t => counts[t] === 0);
  if (missing.length === 1 && selected.length >= 3) {
    const tagToReplace = tags.find(t => counts[t] > 1);
    if (tagToReplace) {
      const idx = selected.findIndex(r => r.tag === tagToReplace);
      const replacement = RECIPES.filter(r => r.tag === missing[0] && !selected.some(s => s.title === r.title));
      if (replacement.length > 0) {
        selected[idx] = replacement[Math.floor(Math.random() * replacement.length)];
      }
    }
  }
  return selected;
}

export function generateRecipesLocally({ pantry, user }) {
  const pantryNames = pantry.map(item => item.name || '');

  const recipesByTag = {
    Desayuno: RECIPES.filter(r => r.tag === 'Desayuno'),
    Comida: RECIPES.filter(r => r.tag === 'Comida'),
    Cena: RECIPES.filter(r => r.tag === 'Cena'),
  };

  const usedTitles = new Set();
  let selected = [];

  const tags = shuffle(['Desayuno', 'Comida', 'Cena']);
  for (const tag of tags) {
    const picked = pickRecipesForTag(recipesByTag[tag], pantryNames, 1, usedTitles);
    if (picked.length > 0) {
      selected.push(picked[0].recipe);
      usedTitles.add(picked[0].recipe.title);
    }
  }

  if (selected.length < 3) {
    const remaining = RECIPES.filter(r => !usedTitles.has(r.title));
    const shuffled = shuffle(remaining);
    for (const r of shuffled) {
      if (selected.length >= 3) break;
      selected.push(r);
      usedTitles.add(r.title);
    }
  }

  selected = distributeMealTypes(selected);

  const result = selected.map((r, i) => {
    const match = scoreRecipe(r, pantryNames);
    return {
      id: `local-recipe-${Date.now()}-${i}`,
      title: r.title,
      subtitle: r.subtitle,
      time: r.time,
      kcal: r.kcal,
      p: r.p,
      c: r.c,
      f: r.f,
      tag: r.tag,
      match: { uses: match.uses, total: match.total },
      img: { hue: Math.floor(Math.random() * 360), sat: 20 },
      why: r.why,
      ingredients: r.ingredients,
      steps: r.steps,
      isAI: false,
    };
  });

  return { recipes: result };
}
